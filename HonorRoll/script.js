// 渲染贡献者列表
function renderContributors() {
    const grid = document.getElementById('contributorsGrid');
    if (!grid) return;
    
    grid.innerHTML = contributorsData.map(contributor => {
        return createContributorCard(contributor);
    }).join('');
}

// 页面加载完成后初始化
document.addEventListener('DOMContentLoaded', () => {
    // 确保DOM完全加载后再执行
    setTimeout(() => {
        try {
            renderMods();
            renderTopContributors();
            renderContributors();
        } catch (error) {
            console.error('渲染错误:', error);
        }
    }, 100);
});



